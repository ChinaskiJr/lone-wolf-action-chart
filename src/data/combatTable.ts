// 0 in [playerLoss, enemyLoss] means instant kill (enemy EP goes to 0)
export const INSTANT_KILL_MARKER = 0

export function lookupCombatResult(
  playerCS: number,
  enemyCS: number,
  randomNumber: number
): [number, number] {
  const ratio = Math.max(-11, Math.min(11, playerCS - enemyCS))
  const row = COMBAT_TABLE[ratio.toString()]
  if (!row) return [0, 0]
  return row[Math.max(0, Math.min(9, randomNumber))]
}
